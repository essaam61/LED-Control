/******************************************************************************
* \Syntax          : void LED_Blink (int color, int ONTime, int OFFTime)
* \Description     : lights a LED for the given ON time, and dim it for the given OFF time.
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : None                   
* \Parameters (out): None                                                      
* \Return value:   : None                          
*******************************************************************************/
void LED_Blink (uint32 color, uint32 ONTime, uint32 OFFTime)
{
    if(Tick_Counter < ONTime)
        SetLED_ON(color);
    else if(Tick_Counter == ONTime) {
        Tick_Counter = RESET;
        while(Tick_Counter < OFFTime)
        {
            SetLED_OFF(color);
        }
        Tick_Counter = RESET;
    }
   
	
}


/******************************************************************************
* \Syntax          : void TimerCount (void) 
* \Description     : Counts the timer ISR ticks.
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : None                   
* \Parameters (out): None                                                      
* \Return value:   : None                          
*******************************************************************************/
void TimerCount (void) 
{
    Tick_Counter++;
}

