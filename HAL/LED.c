/******************************************************************************
* \Syntax          : void SetLED_ON (uint32 LEDColor)
* \Description     : Writes on LED Channel to set it on.                            
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : LEDColor (Numberic ID of LED Channel)                   
* \Parameters (out): None                                                      
* \Return value:   : None                          
*******************************************************************************/
void SetLED_ON (uint32 LEDColor)
{
    Dio_WriteChannel(LEDColor, STD_HIGH);
}

/******************************************************************************
* \Syntax          : void SetLED_OFF (uint32 LEDColor)
* \Description     : Writes on LED Channel to set it off.                            
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : LEDColor (Numberic ID of LED Channel)                     
* \Parameters (out): None                                                      
* \Return value:   : None                          
*******************************************************************************/
void SetLED_OFF (uint32 LEDColor)
{
    Dio_WriteChannel(LEDColor, STD_LOW);
}

/******************************************************************************
* \Syntax          : void Toggle_LED (uint32 LEDColor)
* \Description     : Writes on LED Channel to toggle it.                            
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Reentrant                                             
* \Parameters (in) : LEDColor (Numberic ID of LED Channel)                    
* \Parameters (out): None                                                      
* \Return value:   : None                          
*******************************************************************************/
void Toggle_LED (uint32 LEDColor)
{
    Dio_FlipChannel(LEDColor);
}

